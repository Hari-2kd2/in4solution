    <h2>Late Policy:</h2>
    <p>Office hours are from 9:30 a.m. to 6:30 p.m. with a 15-minute grace period. Any punches
        after 9:45 a.m. will be considered late. Three instances of lateness are tolerated;
        after the third, each late occurrence will result in a deduction of half a day's salary.
    </p>

    <h2>Leave Policy:</h2>
    <p>The annual casual leave entitlement is 12 days. One paid leave will be credited each
        month. Unused leaves can be carried over until the end of the year.</p>
    <p>Even if there are remaining leave days, uninformed leaves and leaves informed on the same
        day will be considered as Loss of Pay (LOP).</p>
    <p>For sick leaves to be valid, a valid medical document and Head of Department's (HOD)
        clearance are required. Casual leaves can only be taken after checking with the HOD and
        obtaining permission.</p>
    <p>If an employee takes a Monday off, Sunday's pay will also be deducted.</p>
    <p>If a staff member works on a Sunday, the first four hours will be counted as one day's
        pay, and the final eight hours will be considered for two days' pay. Employees must use
        the mobile app to check in and out.</p>

    <h2>Hierarchy Levels:</h2>
    <p>There are two levels of hierarchy: HOD and Functional Head. HODs can approve, reject, or
        pass the approval process to the next hierarchy level. This applies to both leave and
        permission requests.</p>

    <h2>Permission Policy:</h2>
    <p>Employees are allowed a total of 4 hours of permission per month (1 hour, 4 times, or 2
        hours, 2 times).</p>

